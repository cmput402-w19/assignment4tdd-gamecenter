    @Test
    public void WinConditions(){
        goBoard gob = new goBoard();
        assertEquals(gob.checkWinner(), 0);

        for (int x=1; x < 5; x++){
            gob.playMove(1, x, 1);
        }
        assertEquals(gob.checkWinner(), 1);

        gob.reset();
        for (int x=1; x < 5; x++){
            gob.playMove(x, 1, 1);
        }
        assertEquals(gob.checkWinner(), 1);

        gob.reset();
        for (int x=1; x < 5; x++){
            gob.playMove(x, x, 1);
        }
        assertEquals(gob.checkWinner(), 1);

    }

    @Test
    public void Score(){
        goBoard gob = new goBoard();

        assertEquals(gob.playerScore, 0);
        assertEquals(gob.computerScore, 0);
        assertEquals(gob.ties, 0);

        for (int x=1; x < 5; x++){
            gob.playMove(1, x, 1);
        }

        gob.checkWinner();

        assertEquals(gob.playerScore, 1);
        assertEquals(gob.computerScore, 0);
        assertEquals(gob.ties, 0);

        gob.reset();

        for (int x=1; x < 5; x++){
            gob.playMove(1, x, -1);
        }

        gob.checkWinner();
        assertEquals(gob.playerScore, 1);
        assertEquals(gob.computerScore, 1);
        assertEquals(gob.ties, 0);

        gob.reset();

        for (int x=1; x < 5; x++){
            gob.playMove(1, x, -1);
        }

        gob.checkWinner();
        assertEquals(gob.playerScore, 1);
        assertEquals(gob.computerScore, 2);
        assertEquals(gob.ties, 0);
    }

